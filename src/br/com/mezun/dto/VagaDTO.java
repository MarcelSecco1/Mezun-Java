
package br.com.mezun.dto;


public class VagaDTO {
    private String nome, observacoes, dataInicio, dataFim, propostaSalarial;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getObservacoes() {
        return observacoes;
    }

    public void setObservacoes(String observacoes) {
        this.observacoes = observacoes;
    }

    public String getPropostaSalarial() {
        return propostaSalarial;
    }

    public void setPropostaSalarial(String propostaSalarial) {
        this.propostaSalarial = propostaSalarial;
    }

    public String getDataInicio() {
        return dataInicio;
    }

    public void setDataInicio(String dataInicio) {
        this.dataInicio = dataInicio;
    }

    public String getDataFim() {
        return dataFim;
    }

    public void setDataFim(String dataFim) {
        this.dataFim = dataFim;
    }
    
    
    
    
    
    
}
